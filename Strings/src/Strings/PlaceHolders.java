package Strings;

public class PlaceHolders {
    public static void main(String[] args) {
        System.out.printf("%c \n",'a');
        System.out.printf("%d \n",2);
        System.out.printf("%e \n",2E3);
        System.out.printf("%f \n",2.43f);
        System.out.printf("%n");
        System.out.println("abs");
    }
}
