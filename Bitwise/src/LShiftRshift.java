public class LShiftRshift {
    public static void main(String[] args) {
        System.out.println("If we right shift bit 1 number will be half");
        int a=10;
        System.out.println(a+" Divivded by 2 = "+(a>>1));
        int b=5;
        System.out.println(b+" Divivded by 2 = "+(b>>1));

        System.out.println("If we left shift bit 1 number will be doubled");
        int c=7;
        System.out.println(c<<1);
    }
}
